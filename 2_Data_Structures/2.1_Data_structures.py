# Sayılar: İnteger
x = 46
type(x)

# Sayılar: Float
y = 55.5
type(y)

# Sayılar: complex
z = 2j + 1
type(z)

# String
x = "Hello ai fatih"
type(x)

# Boolean
x = True
type(x)
5 == 4

# Listeler
x = ["btc", "etc", "xrp"]
type(x)

# Sözlük (dictionary)
x = {"name": "peter", "Age": 12}
type(x)

# Tuple
x = ("python", "merhaba", "nasılsın")
type(x)

# Set
x = {"python", "merhaba", "nasılsın"}
type(x)

# Not: Liste, Tuple, set ve dictionary veri yapıları aynı zamanda Python Collections (Arrays) olarak geçmektedir.
